				<div class="section-title">
					<h2>Veille Technologique</h2> 
				</div>
				<h1 class="text-center mb-5">L'évolution de Bootstrap 4 a 5 </h1>
				<img src="assets/img/v5-new-logo.png" alt="" class="img-fluid mb-5">

				<div data-aos="fade-up">
					<h3 class="text-center">La sortie de Bootstrap 5 </h3>
					<p>
						<ul>
							<li>Sortie <strong>Le 6 mai 2021</strong></li>
						</ul>
						Après plus de <b>trois ans</b> de développement,<b> Bootstrap 5</b> est officiellement disponible. 
						Il <b>rompt</b> effectivement les liens avec la <b>bibliothèque jQuery pour du pur JavaScript.</b>
						Les autres nouveautés de cette version incluent un <b>nouveau logo, un nouveau composant offcanvas, nouveau composant accordion,
						nouveaux composants forms et mise à jour, nouvelle API d'utilitaires etc...</b>
					</p>
				</div>

				<div data-aos="fade-up">
					<h3 class="text-center">Les changements</h3>
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> Un nouveau logo</li>
						</ul>
						L'un des plus grands changements de la version 5 de Bootstrap a été la refonte du logo et la mise à jour du design de la documentation.
						<br/>
						Les nouveaux documents sont plus clairs, mieux organisés avec de nouvelles sections de contenu, et la navigation est également améliorée.
					</p>
				</div>

				<div data-aos="fade-up">
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> JavaScript</li>
						</ul>
						La plus grande modification en rapport avec <b>JavaScript</b> a été la suppression de <b>jQuery</b>.<br/>
						Un certain nombre d'<b>améliorations</b> ont été apporté : <br/>
						<ul>
							<ul>
								<li>Tous les plugins peuvent désormais accepter un sélecteur CSS comme premier argument . </li>
								<li>Mise à jour vers Popper 2</li>
								<li>Tous les plugins JavaScript sont maintenant espacés par des noms avec bs (exemple: vous pouvez utiliser data-bs-toggle au lieu de data-toggle).</li>
								<li>Revu du  positionnement de dropdown, popover et tooltip.</li>
								<li>Revu du positionnement des toasts.</li>
								<li>Possibilité d'utiliser des classes personnalisées pour les infobulles et les popovers</li>
								<li>Diverses optimisations ont été effectuées pour mieux partager le code entre les composants</li>
								<li>Modification des dropdowns pour émettre des événements sur .dropdown-toggle au lieu de .dropdown.</li>
								<li>Renommage de whiteList en allowList dans les popovers et les tooltips</li>
							</ul>
						</ul>
				</div>
				<div data-aos="fade-up">
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> Composant offcanvas</li>
						</ul>
						Basé sur les éléments fondamentaux des modals, le nouveau composant offcanvas est doté d'une toile de fond, d'un défilement du corps et d'un placement configurables. Les composants offcanvas peuvent être placés en haut, à droite, en bas ou à gauche de la fenêtre d'affichage. 
					</p>
				</div>
				<div data-aos="fade-up">
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> Composant accordion</li>
						</ul>
						Remplacement du composant accordéon .card par un tout nouveau composant .accordion. Ce nouveau composant utilise toujours le plug-in JavaScript Collapse, mais avec un HTML et un CSS personnalisés pour le prendre en charge. Le composant .accordion-flush sert à supprimer les bordures extérieures, ce qui permet de le placer plus facilement dans les éléments parents.
					</p>
				</div>
				<div data-aos="fade-up">
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> Composants forms et mise à jour</li>
						</ul>
						Regroupement, dans la documentation, de tous les styles de formulaires dans une nouvelle section consacrée aux formulaires et ils ont redessiné tous les contrôles de formulaires.
					<br/><br/>
						<b>PS:</b> Dans la version 4, elle a introduit une suite étendue de contrôles de formulaires personnalisés, notamment contrôles, radios, interrupteurs, fichiers, etc., mais ceux-ci s'ajoutaient aux valeurs par défaut fournies par chaque navigateur. Avec la version 5, l'équipe a opté pour une personnalisation totale.
					</p>
				</div>

				<div data-aos="fade-up">
					<p>
						<ul>
							<li class="fw-bolder"><i class="bi bi-flower2"></i> Case à cocher et bouton radio</li>
						</ul>
						Chaque case à cocher (chaque case d'option, chaque sélection, chaque fichier, chaque plage, etc.) comprend une apparence personnalisée afin d'unifier le style et le comportement des contrôles de formulaires, quels que soient le système d'exploitation et le navigateur.
					</p>
				</div>
				<div data-aos="fade-up">
